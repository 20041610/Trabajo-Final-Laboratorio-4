"""
Realizar la carga del lado de un cuadrado, 
mostrar por pantalla el perímetro del mismo 
(El perímetro de un cuadrado se
calcula multiplicando el valor del lado por cuatro)
"""
ladoDelCuadrado = int(input("Ingrese el lado de un cuadrado: "))
perimetroDelCuadrado= ladoDelCuadrado * 4
print("El perimetro del cuadrado es: ", perimetroDelCuadrado)